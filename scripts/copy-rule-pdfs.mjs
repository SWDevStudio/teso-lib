import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const RULES_DIR = path.join(ROOT, 'rules')
const PUBLIC_DIR = path.join(ROOT, 'public', 'rules')
const OUT_TS = path.join(ROOT, 'src', 'assets', 'data', 'rule-originals.ts')

// slug свода → исходный PDF (своды конвертированы из .docx в PDF вручную).
// slug должен совпадать со slug в scripts/build-rules.mjs.
const PDFS = {
  obschie: 'Obschie_pravila_TES-2026_v2_2.pdf',
  economy: '1_Pravila_po_ekonomike_v1_6_1_5.pdf',
  alchemy: 'Pravila_Alkhimia_TES-2026_v1_0.pdf',
  smithing: 'Pravila_Kuznechestvo_i_Yuvelirka_TES-2026_v1_0.pdf',
  gardening: 'Pravila_SadovodstvoTES-2026_v2_0.pdf',
  faith: 'Pravila_Vera_religia_i_zhrechestvo_TES-2026_v1_0.pdf',
  enchanting: 'Pravila_Zacharovanie_TES-2026_v1_0.pdf',
}

fs.mkdirSync(PUBLIC_DIR, { recursive: true })

const map = {}
for (const [slug, file] of Object.entries(PDFS)) {
  const src = path.join(RULES_DIR, file)
  if (!fs.existsSync(src)) {
    console.warn(`пропуск ${slug}: нет ${file}`)
    continue
  }
  const dest = path.join(PUBLIC_DIR, `${slug}.pdf`)
  fs.copyFileSync(src, dest)
  map[slug] = `/rules/${slug}.pdf`
  console.log(`${slug.padEnd(12)} ←  ${file}  (${Math.round(fs.statSync(dest).size / 1024)}KB)`)
}

const ts = `// AUTO-GENERATED by scripts/copy-rule-pdfs.mjs — НЕ редактировать вручную.
// Источник: rules/*.pdf. Пересборка: npm run rules:pdf
export const ruleOriginalPdfs: Record<string, string> = ${JSON.stringify(map, null, 2)}
`
fs.writeFileSync(OUT_TS, ts, 'utf8')
console.log(`\n✓ ${OUT_TS}  (${Object.keys(map).length} PDF)`)
